import sys

# Type Function

# String
print(type("Hello World"))

# Integer
print(type(15))
print(type(-15))
print(type(0))
print(type(1))

# Float
print(type(3.14))
print(type(0.5))
print(type(1.0))
print(type(-5.0))

# Boolean
print(type(True))
print(type(False))



# To obtain the information about 'Integer' and 'float'
print(sys.int_info)
print() # To add a space between two outputs, use 'print()' function  
print(sys.float_info)